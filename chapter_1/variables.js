console.log("================== VARIABLES ==================");

// Variables
// let variable => newer version of "var"
// let and var value can be changed
let age = 20;
let year = 2023;

// Log things to console
console.log(age, year);

// Update the age value to 30
age = 30;
console.log(age);

// The const value can not be changed
const point = 100;
console.log(point);

// var is old data type (used in code written for older browsers)
var score = 75;
console.log(score);